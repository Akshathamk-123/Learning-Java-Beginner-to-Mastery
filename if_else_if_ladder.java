public class if_else_if_ladder {
    public static void main(String arg[])
    {
        int x=6;
        int y=8;
        int z=10;

        if(x>z)
        {
            System.out.println(x);
        }
        else if(y>x)
        {
            System.out.println(y);
        }
        else
        {
            System.out.println(z);
        }
    }
}
